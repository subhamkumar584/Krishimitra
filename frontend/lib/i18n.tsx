function translate(lang: LangCode, key: string) {
  return translations[lang][key] || translations.en[key] || key;
}

export type I18nContextValue = {
  lang: LangCode;
  setLang: (l: LangCode) => void;
  t: (key: string) => string;
};

const I18nContext = createContext<I18nContextValue | null>(null);

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<LangCode>("en");
  useEffect(() => {
    try {
      const saved = localStorage.getItem(KEY);
      if (saved === "en" || saved === "hi" || saved === "or") setLangState(saved);
    } catch {}
  }, []);
  const setLang = (l: LangCode) => {
    setLangState(l);
    try { localStorage.setItem(KEY, l); } catch {}
    try { if (typeof document !== 'undefined') document.documentElement.lang = l; } catch {}
  };
  useEffect(() => {
    try { if (typeof document !== 'undefined') document.documentElement.lang = lang; } catch {}
  }, [lang]);
  const value = useMemo(() => ({ lang, setLang, t: (k: string) => translate(lang, k) }), [lang]);
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n must be used within LanguageProvider");
  return ctx;
}